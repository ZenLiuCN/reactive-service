package cn.zenliu.reactive.service.plugin;

import lombok.experimental.UtilityClass;


/**
 * Basic Plugin interface
 */
public interface Plugin {


    /**
     * scope class is something just hidden inner content for outside
     */
    @UtilityClass
    class scope {
        RuntimeException instanceError = new RuntimeException("instance plugin failed");

        public RuntimeException InstanceErrorSuppler() {
            return instanceError;
        }
    }
}
